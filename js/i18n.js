let lang = localStorage.getItem('inkwell_web_lang') || 'de';
const langChangeListeners = [];

function t(k) {
  return T[lang][k] || k;
}

function tf(k, vars) {
  const base = t(k);
  return base.replace(/\{(\w+)\}/g, (_, key) => vars[key] || '');
}

function setLang(l) {
  if (!T[l]) return;
  lang = l;
  localStorage.setItem('inkwell_web_lang', l);

  // Sync lang switcher buttons across the UI
  document.querySelectorAll('.lb').forEach(b => {
    const btnText = b.textContent.trim().toLowerCase();
    b.classList.toggle('active', btnText === l);
  });

  const mobileLangBtn = document.getElementById('lang-btn-mobile');
  if (mobileLangBtn) {
    const textSpan = mobileLangBtn.querySelector('span');
    if (textSpan) textSpan.textContent = l.toUpperCase();
  }

  // Update HTML elements carrying dynamic attributes
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    const val = t(key);
    if (val) el.innerHTML = val;
  });

  document.querySelectorAll('[data-i18n-ph]').forEach(el => {
    const key = el.getAttribute('data-i18n-ph');
    const val = t(key);
    if (val) el.placeholder = val;
  });

  document.documentElement.lang = l;

  // Run additional callbacks (e.g. to update version texts on main page, or sync statuses on dashboard)
  langChangeListeners.forEach(listener => {
    try {
      listener(l);
    } catch (e) {
      console.error('Error in language listener:', e);
    }
  });
}

function addLangChangeListener(fn) {
  if (typeof fn === 'function') {
    langChangeListeners.push(fn);
  }
}

// Automatically initialize translation on DOM Content Loaded
document.addEventListener('DOMContentLoaded', () => {
  setLang(lang);
});
